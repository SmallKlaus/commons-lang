package org.apache.commons.lang3.arch;

/**
 * The {@link ProcessorType} enum defines types of a microprocessor.
 * The following types are defined:
 * <ul>
 *     <li>x86</li>
 *     <li>ia64</li>
 *     <li>ppc</li>
 *     <li>unknown</li>
 * </ul>
 */
public enum ProcessorType {
    X86, IA_64, PPC, UNKNOWN
}
